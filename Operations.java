public class Operations{

    public int getsum1(int[] array){
        int sum1 = 0;
        for(int i = 1; i < array.length; i++){
            sum1 = sum1 + array[i];
        }
        return sum1;
    }

    public int getsum2(int[] array){
        int sum2 = 0;
        for(int i = 1; i < array.length; i++){
            sum2 = sum2 + array[i];
        }
        return sum2;
    }
    
}